// operation add 2 number
function add(num1, num2) {
  return num1 + num2
}

// operation subtraction 2 number
function subtraction(num1, num2) {
  return num1 - num2
}

// operation multiplication 2 number
function multiplication(num1, num2) {
  return num1 * num2
}

// operation division 2 number
function division(num1, num2) {
  return num1 / num2
}

// operation modulus 2 number
function mod(num1, num2) {
  return num1 % num2
}

// example of use
console.log(add(91, 27));
console.log(subtraction(91, 27));
console.log(multiplication(91, 27));
console.log(division(10, 2));
console.log(mod(93, 6));